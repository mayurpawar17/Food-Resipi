<br><br><h2>HOW TO MAKE VEG BIRIYANI WITH STEP BY STEP </h2><br><br>


<h4>How to marinate vegetables:</h4><br>
[1]firstly, in a large bowl take 1 cup curd, 1 tsp ginger garlic paste, 2 chilli, 2 tsp lemon juice, 1 pack shan biryani masala and ½ tsp salt.
<br>[2] mix well combining all the ingredients well.
<br>[3]add 1 potato, 1 carrot, ½ capsicum, 5 beans, 3 tbsp coriander and 3 tbsp mint.
<br>[4]cover and marinate for 30 minutes or more.


<br><br><h4>How to make veg biryani gravy:</h4><br>
<br>[1]firstly, in a large kadai heat 2 tbsp ghee and 2 tbsp oil.
<br>[2]saute 1 onion, 1 tsp ginger garlic paste and saute well.
<br>[3]saute until the onions turn golden brown.
<br>[4]now add marinated vegetables and cook until the oil separates.
<br>[5]once the vegetables are almost cooked add 2 tbsp fried onion and mix well.
<br>[6]biryani base is ready.
<br>[7]Now add 1 cup water and mix well adjusting the consistency.
<br>[8]Further, add 2 tbsp cream and mix well.
<br>[9]Also, add 16 cubes of paneer and mix gently.
<br>[10]Simmer for 5 minutes, or until the paneer absorbs the flavour.
<br>[11]Add 1 tsp kasuri methi and 2 tbsp coriander. Mix well.
<br>[12]Finally, enjoy paneer butter masala with roti or naan.

<br><br><h4>how to make biryani rice:</h4><br>
<br>[1]firstly, rinse and soak 2 cups of basmati rice for 20 minutes.
<br>[2]in a large vessel take enough water. also add 2 bay leaf, 2 inch cinnamon, 1 pod black cardamom, 2 pods cardamom, 1 javitri, 6 cloves and 1 star anise.
<br>[3]further add 2 tsp ghee, 1 tsp lemon juice, 1 tsp cumin, ½ tsp pepper and 2 tsp salt.
<br>[4]mix well and get to a rolling boil.
<br>[5]add in soaked rice and mix well.
<br>[6]boil the rice until it is almost cooked.
<br>[7]drain off the rice and keep aside.
<br>[8]inally, enjoy veg biriyani recipe.


